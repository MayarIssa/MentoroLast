interface EmojiPickerProps {
  onEmojiSelect: (emoji: string) => void;
  children: React.ReactNode;
}

export function EmojiPicker({ onEmojiSelect, children }: EmojiPickerProps) {
  const [isOpen, setIsOpen] = useState(false);

  const handleEmojiClick = (emoji: string) => {
    onEmojiSelect(emoji);
    setIsOpen(false);
  };

  return (
    <Popover open={isOpen} onOpenChange={setIsOpen}>
      <PopoverTrigger asChild>{children}</PopoverTrigger>
      <PopoverContent
        className="w-80 p-0"
        side="top"
        align="start"
        sideOffset={8}
      >
        <Tabs defaultValue="smileys" className="w-full">
          <div className="border-b p-2">
            <TabsList className="grid h-8 w-full grid-cols-4">
              {Object.entries(emojiCategories)
                .slice(0, 4)
                .map(([key, category]) => {
                  const IconComponent = category.icon;
                  return (
                    <TabsTrigger key={key} value={key} className="h-6 px-2">
                      <IconComponent className="h-3 w-3" />
                    </TabsTrigger>
                  );
                })}
            </TabsList>
            <TabsList className="mt-1 grid h-8 w-full grid-cols-4">
              {Object.entries(emojiCategories)
                .slice(4)
                .map(([key, category]) => {
                  const IconComponent = category.icon;
                  return (
                    <TabsTrigger key={key} value={key} className="h-6 px-2">
                      <IconComponent className="h-3 w-3" />
                    </TabsTrigger>
                  );
                })}
            </TabsList>
          </div>

          {Object.entries(emojiCategories).map(([key, category]) => (
            <TabsContent key={key} value={key} className="mt-0">
              <ScrollArea className="h-48 p-2">
                <div className="grid grid-cols-8 gap-1">
                  {category.emojis.map((emoji, index) => (
                    <Button
                      key={`${emoji}-${index}`}
                      variant="ghost"
                      size="sm"
                      className="hover:bg-muted h-8 w-8 p-0 text-lg"
                      onClick={() => handleEmojiClick(emoji)}
                    >
                      {emoji}
                    </Button>
                  ))}
                </div>
              </ScrollArea>
            </TabsContent>
          ))}
        </Tabs>
      </PopoverContent>
    </Popover>
  );
}
